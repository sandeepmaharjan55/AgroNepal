Major cultivated species of wheat</center><p>
Hexaploid species<p>

Common wheat or bread wheat (T. aestivum) – A hexaploid species that is the most widely cultivated in the world.
Spelt (T. spelta) – Another hexaploid species cultivated in limited quantities. Spelt is sometimes considered a subspecies of the closely related species common wheat (T. aestivum), in which case its botanical name is considered to be Triticum aestivum subsp. spelta.<p>
Tetraploid species<p>

Durum (T. durum) – The only tetraploid form of wheat widely used today, and the second most widely cultivated wheat.
Emmer (T. dicoccon) – A tetraploid species, cultivated in ancient times but no longer in widespread use.
Khorasan (Triticum turgidum ssp. turanicum also called Triticum turanicum) is a tetraploid wheat species.[2] It is an ancient grain type; Khorasan refers to a historical region in modern-day Afghanistan and the northeast of Iran. This grain is twice the size of modern-day wheat and is known for its rich nutty flavor.[3]
Diploid species<p>

Einkorn (T. monococcum) – A diploid species with wild and cultivated variants. Domesticated at the same time as emmer wheat, but never reached the same importance.<p>
Classes used in the United States:
<p>
Durum – Very hard, translucent, light-colored grain used to make semolina flour for pasta & bulghur; high in protein, specifically, gluten protein.<p>
Hard Red Spring – Hard, brownish, high-protein wheat used for bread and hard baked goods. Bread Flour and high-gluten flours are commonly made from hard red spring wheat. It is primarily traded at the Minneapolis Grain Exchange.<p>
Hard Red Winter – Hard, brownish, mellow high-protein wheat used for bread, hard baked goods and as an adjunct in other flours to increase protein in pastry flour for pie crusts. Some brands of unbleached all-purpose flours are commonly made from hard red winter wheat alone. It is primarily traded on the Kansas City Board of Trade. One variety is known as "turkey red wheat", and was brought to Kansas by Mennonite immigrants from Russia.[40]<p>
Soft Red Winter – Soft, low-protein wheat used for cakes, pie crusts, biscuits, and muffins. Cake flour, pastry flour, and some self-rising flours with baking powder and salt added, for example, are made from soft red winter wheat. It is primarily traded on the Chicago Board of Trade.<p>
Hard White – Hard, light-colored, opaque, chalky, medium-protein wheat planted in dry, temperate areas. Used for bread and brewing.<p>
Soft White – Soft, light-colored, very low protein wheat grown in temperate moist areas. Used for pie crusts and pastry. Pastry flour, for example, is sometimes made from soft white winter wheat.
